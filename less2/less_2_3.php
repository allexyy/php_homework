<?php
function sum($num1,$num2){
    return $num1+$num2;
}
    
function multiply($num1,$num2){
    return $num1*$num2;
}
function sub($num1,$num2){
    return $num1-$num2;
}
function split($num1,$num2){
    return $num1/$num2;
}
echo(sum(2,3)."<br>");
echo(multiply(2,3)."<br>");
echo(sub(2,3)."<br>");
echo(split(2,3)."<br>");

?>